<!-- 歌单内的播放列表组件 -->
<template>
  <div class="playSongList">
    <div class="song-detail" @click="clickToPlay(index)">  
      <i class="icon-volume-medium left" v-show="currentIndex == index"></i>
      <p class="song-index left" v-show="currentIndex != index">{{ index+1 }}</p>
      <div class="song-info">
        <p class="song-name">{{ list.name }}</p>
        <p class="song-singer">{{ list.singer }}</p>  
      </div>
    </div>
    <div class="border-1px"></div>
    <i class="icon-list-circle more" @click="showMoreOperation(list.name)"></i>
  </div>
</template>

<script>
export default {
  data () {
  	return {
      //不能给每个组件绑定一个当前index数据
  	}
  },
  props: ['list', 'index'],
  computed: {
    currentIndex () {
      return this.$store.getters.getPlayingSongIndex;
    }
  },
  methods: {
    clickToPlay (index) {
      let obj = {};
      obj.index = index;
      obj.songInfo = this.list;
      this.$store.commit('playIndexMusic', obj);
    },
    showMoreOperation (songName) {
      this.$store.commit('showOperation', {
        songName: songName,
        iconInfo: [
          {
            iconText: '收藏到歌单',
            iconClass: 'icon-add-project'
          },
          {
            iconText: '下载',
            iconClass: 'icon-download'
          },
          {
            iconText: '评论' + '(2333)',
            iconClass: 'icon-msg'
          },
          {
            iconText: '分享',
            iconClass: 'icon-share'
          },
          {
            iconText: '歌手:' + this.list.singer,
            iconClass: 'icon-collect'
          },
          {
            iconText: '删除',
            iconClass: 'icon-delete'
          }
        ]
      })
    }
  }
}
</script>

